async function connectContract() {
  if (typeof window.ethereum === "undefined") {
    alert("Please install MetaMask");
    return null;
  }
  const provider = new ethers.providers.Web3Provider(window.ethereum);
  await provider.send("eth_requestAccounts", []);
  const signer = provider.getSigner();
  return new ethers.Contract(contractAddress, contractABI, signer);
}

async function mintNFT() {
  const contract = await connectContract();
  if (!contract) return;

  // Gather form data
  const name = document.querySelector("input[placeholder=\"Input Room’s Name…\"]").value;
  const description = document.querySelector(".rentdesc").value;
  const location = document.querySelector("input[placeholder='Input Location…']").value;
  const rentCost = document.querySelector("input[placeholder='Input Rent Cost (NTD)']").value;
  const deposit = document.querySelector("input[placeholder='Input Deposit Cost (NTD)']").value;

  if (!name || !description || !location || !rentCost || !deposit) {
    alert("Please fill out all required fields.");
    return;
  }

  // Convert to integer (if using NTD) or to wei (if using ETH in contract)
const rentCostVal = 1; // lowest possible price (1 wei)
const depositVal = 1;

  // Prepare tokenURI: encode metadata as JSON with name, description, image
  const imageInput = document.getElementById("imageInput");
  const file = imageInput.files[0];
  if (!file) {
    alert("Please upload an image.");
    return;
  }

  const reader = new FileReader();
  const imageLink = document.getElementById("imageLinkInput").value.trim();
  reader.onloadend = async function () {
const metadata = {
  name: name,
  description: description,
  image: imageLink
};
const tokenURI = "data:application/json;base64," + btoa(JSON.stringify(metadata));

    try {
      // Mint NFT (location, rentPrice, deposit, tokenURI)
      const tx = await contract.mint(location, rentCostVal, depositVal, tokenURI);
      await tx.wait();
      alert("NFT Minted Successfully!\nTx Hash: " + tx.hash);
    } catch (err) {
      console.error("Minting failed:", err);
      alert("Minting failed. Check console.");
    }
  };
  reader.readAsDataURL(file);
}

// Hook up the Mint Button
window.addEventListener("DOMContentLoaded", () => {
  const mintButton = document.getElementById("mintButton");
  if (mintButton) {
    mintButton.addEventListener("click", mintNFT);
  }

  const imageLinkInput = document.getElementById('imageLinkInput');
  if (imageLinkInput) {
    imageLinkInput.addEventListener('input', function(e) {
      const val = e.target.value.trim();
      if (val) {
        document.getElementById('preview').src = val;
      }
    });
  }
});