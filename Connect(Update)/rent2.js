async function signLease({ tokenId = 0, rentalPeriod = 1 } = {}) {
  if (typeof window.ethereum === "undefined") {
    alert("Please install MetaMask.");
    return;
  }

  try {
    const provider = new ethers.providers.Web3Provider(window.ethereum);
    await provider.send("eth_requestAccounts", []);
    const signer = provider.getSigner();
    const contract = new ethers.Contract(contractAddress, abi, signer);

    // Get property details from contract
    const property = await contract.properties(tokenId);
    const total = (BigInt(property.rentPrice) * BigInt(rentalPeriod)) + BigInt(property.deposit);

    // === Balance check ===
    const balance = await provider.getBalance(await signer.getAddress());
    if (balance < total) {
      alert("Insufficient funds! You need at least " + ethers.utils.formatEther(total) + " ETH plus gas.");
      return;
    }
    // Execute rent transaction
    const tx = await contract.rent(tokenId, rentalPeriod, {
      value: total.toString()
    });

    alert("Lease signed! Tx Hash: " + tx.hash);

    // Optionally update localStorage and redirect
     const now = new Date();
    const startDate = now.toISOString().slice(0, 10);
    const endDate = new Date(now.getTime() + rentalPeriod * 24 * 60 * 60 * 1000)
                      .toISOString().slice(0, 10);

    const newLease = {
      roomType: property.location,
      startDate,
      endDate,
      price: ethers.utils.formatEther(property.rentPrice) + " ETH / night",
      status: "In progress",
      image: "../images/hotel3.jpg"
    };
    let rentals = JSON.parse(localStorage.getItem("rentalHistory")) || [];
    rentals.push(newLease);
    localStorage.setItem("rentalHistory", JSON.stringify(rentals));
    window.location.href = "../DataHistory/datahistory.html";

  } catch (err) {
    console.error(err);
    alert("Failed to sign lease: " + (err.data?.message || err.message));
  }
}

window.addEventListener("DOMContentLoaded", () => {
  // Select all sign buttons
  document.querySelectorAll(".sign").forEach(btn => {
    btn.addEventListener("click", function () {
      // Get tokenId from the data attribute
      const tokenId = parseInt(this.getAttribute("data-tokenid"), 10);
      const rentalPeriod = 1; // or prompt user for rental period
      signLease({ tokenId, rentalPeriod });
    });
  });
});